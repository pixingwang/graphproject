package graph.server.model;

import java.util.Date;

import org.springframework.format.annotation.DateTimeFormat;

public class GraphXModel {

	private long graphX_id; // graphX的ID
	private String graphX_name; // graphX的名称
	private String graphX_ip; // graphX的IP
	private String graphX_port; // graphX的端口
	private long user_Id; // 平台登陆用户ID
	private String graphX_version; // graphX的版本
	private String graphX_algorithm; // graphX的算法
	private String graphX_operation; // graphX的操作
	
	@DateTimeFormat(pattern = "yyyy-MM-dd HH:mm:ss")
	private Date graphX_createtime; //graphX的创建时间
	
	public Date getGraphX_createtime() {
		return graphX_createtime;
	}

	public void setGraphX_createtime(Date graphX_createtime) {
		this.graphX_createtime = graphX_createtime;
	}

	public long getGraphX_id() {
		return graphX_id;
	}

	public void setGraphX_id(long graphX_id) {
		this.graphX_id = graphX_id;
	}

	public String getGraphX_name() {
		return graphX_name;
	}

	public void setGraphX_name(String graphX_name) {
		this.graphX_name = graphX_name;
	}

	public String getGraphX_ip() {
		return graphX_ip;
	}

	public void setGraphX_ip(String graphX_ip) {
		this.graphX_ip = graphX_ip;
	}

	public String getGraphX_port() {
		return graphX_port;
	}

	public void setGraphX_port(String graphX_port) {
		this.graphX_port = graphX_port;
	}

	public long getUser_Id() {
		return user_Id;
	}

	public void setUser_Id(long user_Id) {
		this.user_Id = user_Id;
	}

	public String getGraphX_version() {
		return graphX_version;
	}

	public void setGraphX_version(String graphX_version) {
		this.graphX_version = graphX_version;
	}

	public String getGraphX_algorithm() {
		return graphX_algorithm;
	}

	public void setGraphX_algorithm(String graphX_algorithm) {
		this.graphX_algorithm = graphX_algorithm;
	}

	public String getGraphX_operation() {
		return graphX_operation;
	}

	public void setGraphX_operation(String graphX_operation) {
		this.graphX_operation = graphX_operation;
	}



	@Override
	public String toString() {
		return "GraphX [graphX_id=" + graphX_id + ", graphX_name=" + graphX_name + ", graphX_ip=" + graphX_ip
				+ ", graphX_port=" + graphX_port + ", user_Id=" + user_Id + ", graphX_version=" + graphX_version
				+ ", graphX_algorithm=" + graphX_algorithm + ", graphX_operation=" + graphX_operation
				+ ", graphX_createtime=" + graphX_createtime + "]";
	}
	
	


}
